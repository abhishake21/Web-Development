// The break statement terminates a loop.

for (i=1; i<=5; i++) {
    if (i===3) {
        break;
    }
    console.log(i);
}
console.log('');





// The continue statement "breaks one iteration in the loop and continues with the next iteration in the loop.

for (i=1; i<=5; i++) {
    if (i===3) {
        continue;
    }
    console.log(i);
}
// In the above loop 4 is missing.

console.log('');



