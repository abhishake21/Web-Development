//  For loop

// printing 1 to 10
for(i=1; i<=10; i++) {
    console.log(i);
}

console.log('');
// Printing tables using for loop
let tableOf = 3;
for(i=1; i<=10; i++) {
    console.log(`${tableOf}x${i}=${tableOf*i}`);
}






console.log('');
// While Loop
let p=1;
while (p<=10) {
    console.log(p);
    p++;
}

console.log('');
// printing table using while loop
let q=1;
while (q<=10) {
    console.log(`${tableOf}x${q}=${tableOf*q}`)
    q++;
}





console.log('');
// Do while Loop - Will always be executed at least once, even if the condition is false.
p=10;
do {
    console.log(p);
    p++;
} while (p<=10);

console.log(p);


